# 2. String Literals and Basic Operations

# String literals
first_name = "Alice"
last_name = "Johnson"
# Concatenation
print(first_name + last_name)
print(first_name,last_name)


# # Repetition
separator = "-" * 20
print(f"Separator: {separator}")

emphasis = "Very " * 3 + "important!"
print(f"Emphasis: {emphasis}")

# # Multiple operations
banner = "=" * 10 + " WELCOME " + "=" * 10
print(f"Banner: {banner}")




